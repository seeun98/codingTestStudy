## [프로그래머스] 오프라인/온라인 판매 데이터 통합하기
https://school.programmers.co.kr/learn/courses/30/lessons/131118#qna
### SOLUTION
```SQL
SELECT TO_CHAR(SALES_DATE, 'YYYY-MM-DD') AS "SALES_DATE", PRODUCT_ID, USER_ID, SALES_AMOUNT
FROM ONLINE_SALE
WHERE TO_CHAR(SALES_DATE, 'YYYY-MM') LIKE '2022-03'
UNION ALL
SELECT TO_CHAR(SALES_DATE, 'YYYY-MM-DD') AS "SALES_DATE", PRODUCT_ID, NULL AS "USER_ID", SALES_AMOUNT
FROM OFFLINE_SALE
WHERE TO_CHAR(SALES_DATE, 'YYYY-MM') LIKE '2022-03'
ORDER BY "SALES_DATE", PRODUCT_ID, USER_ID NULLS LAST
```

### 참고
- OFFLINE_SALE에 없는 데이터인 USER_ID는 NULL로 컬럼을 하나 더 추가해서 출력하도록 한다.
- 합쳐서 NVL을 사용하기엔 어려움
- UNION ALL을 잘 사용하자 
- ORDER BY까지 똑같아야 UNION ALL 가능 ! 
- 나오는 컬럼과, ORDER BY까지